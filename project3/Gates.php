			<h2>Philanthropy - Bill and Melinda Gates Foundation</h2>
			<p>
				Gates studied the work of Andrew Carnegie and John D. Rockefeller, and donated some of his Microsoft stock in 1994 to create the "William H. Gates Foundation." In 2000, Gates and his wife combined three family foundations and Gates donated stock valued at $5 billion to create the charitable Bill & Melinda Gates Foundation, which was identified by the Funds for NGOs company in 2013, as the world's wealthiest charitable foundation, with assets reportedly valued at more than $34.6 billion.The Foundation allows benefactors to access information that shows how its money is being spent, unlike other major charitable organizations such as the Wellcome Trust. Gates, through his foundation, also donated $20 million to Carnegie Mellon University(CMU) for a new building to be named Gates Center for Computer Science which opened in 2009
			</p>

			<p>
				Gates has credited the generosity and extensive philanthropy of David Rockefeller as a major influence. Gates and his father met with Rockefeller several times, and their charity work is partly modeled on the Rockefeller family's philanthropic focus, whereby they are interested in tackling the global problems that are ignored by governments and other organizations.[ As of 2007, Bill and Melinda Gates were the second-most generous philanthropists in America, having given over $28 billion to charity; the couple plan to eventually donate 95 percent of their wealth to charity.
			</p>
			</section>
		
			<section>
				<h2>Microsoft - BASIC</h2>
					<p>
					MITS Altair 8800 Computer with 8-inch (200 mm) floppy disk system. After Gates read the January 1975 issue of Popular Electronics, which demonstrated the Altair 8800, he contacted Micro Instrumentation and Telemetry Systems (MITS), the creators of the new microcomputer, to inform them that he and others were working on a BASIC interpreter for the platform. In reality, Gates and Allen did not have an Altair and had not written code for it; they merely wanted to gauge MITS's interest. MITS president Ed Roberts agreed to meet them for a demo, and over the course of a few weeks they developed an Altair emulator that ran on a minicomputer, and then the BASIC interpreter. The demonstration, held at MITS's offices in Albuquerque, was a success and resulted in a deal with MITS to distribute the interpreter as Altair BASIC. Paul Allen was hired into MITS, and Gates took a leave of absence from Harvard to work with Allen at MITS in Albuquerque in November 1975. They named their partnership "Micro-Soft" and had their first office located in Albuquerque. Within a year, the hyphen was dropped, and on November 26, 1976, the trade name "Microsoft" was registered with the Office of the Secretary of the State of New Mexico. Gates never returned to Harvard to complete his studies.
					</p>

