			<h2>Banned from school</h2>
			<figure>
  				<img src="images/pic.jpg" alt="malala">  
  				<figcaption>Malala</figcaption>
			</figure>
				<p><strong>Following </strong> the edict, the Taliban destroyed several more local schools. On 24 January 2009, Yousafzai wrote: "Our annual exams are due after the vacations but this will only be possible if the Taliban allow girls to go to school. We were told to prepare certain chapters for the exam but I do not feel like studying."In February 2009, girls' schools were still closed. In solidarity, private schools for boys had decided not to open until 9 February, and notices appeared saying so. On 7 February, Yousafzai and her brother returned to their hometown of Mingora, where the streets were deserted, and there was an "eerie silence". "We went to the supermarket to buy a gift for our mother but it was closed, whereas earlier it used to remain open till late. Many other shops were also closed", she wrote in her blog. Their home had been robbed and their television was stolen.</p>

				<p>After boys' schools reopened, the Taliban lifted restrictions on girls' primary education, where there was co-education. Girls-only schools were still closed. Yousafzai wrote that only 70 pupils attended, out of 700 pupils who were enrolled. Ziauddin Yousafzai, who is a poet, school owner, and an educational activist himself, running a chain of private schools known as the Khushal Public School. In an interview, Yousafzai once stated that she aspired to become a doctor, though later her father encouraged her to become a politician instead. Ziauddin referred to his daughter as something entirely special, allowing her to stay up at night and talk about politics after her two brothers had been sent to bed.</p>

				<p>On 15 February, gunshots could be heard in the streets of Mingora, but Yousafzai's father reassured her, saying "Don't be scared – this is firing for peace." Her father had read in the newspaper that the government and the militants were going to sign a peace deal the next day. Later that night, when the Taliban announced the peace deal on their FM Radio studio, another round of stronger firing started outside.Yousafzai spoke out against the Taliban on the national current affairs show Capital Talk on 18 February. Three days later, local Taliban leader Maulana Fazlulla announced on his FM radio station that he was lifting the ban on women's education, and girls would be allowed to attend school until exams were held on 17 March, but they had to wear burqas.</p>
