			<div class="p1">
				<p><strong>Malala Yousafzai</strong> (Malālah Yūsafzay: Urdu: ملالہ یوسفزئی‬‎; Pashto: ملاله یوسفزۍ‎ [məˈlaːlə jusəf ˈzəj]; born 12 July 1997) is a Pakistani activist for female education and the youngest Nobel Prize laureate. She is known for human rights advocacy, especially the education of women and children in her native Swat Valley in Khyber Pakhtunkhwa, northwest Pakistan, where the local Taliban had at times banned girls from attending school. Her advocacy has grown into an international   movement, and according to former Pakistani Prime Minister Shahid Khaqan Abbasi, she has become "the most prominent citizen" of the country.</p>
			</div>
				<p class="subp1">
				Yousafzai was born to a Pashtun family in Mingora, Khyber Pakhtunkhwa, Pakistan. Her family came to run a chain of schools in the region. Considering Muhammad Ali Jinnah and Benazir Bhutto as her role models, she was particularly inspired by her father's thoughts and humanitarian work. In early 2009, when she was 11–12, she wrote a blog under a pseudonym for the BBC Urdu detailing her life during the Taliban occupation of Swat. The following summer, journalist Adam B. Ellick made a New York Times documentary about her life as the Pakistani military intervened in the region. She rose in prominence, giving interviews in print and on television, and she was nominated for the International Children's Peace Prize by activist Desmond Tutu.

				Following her recovery, Yousafzai became a prominent activist for the right to education. Based out of Birmingham, she founded the Malala Fund, a non-profit organization and in 2013 co-authored I Am Malala, an international best seller. In 2012, she was the recipient of Pakistan's first National Youth Peace Prize and the 2013 Sakharov Prize. In 2014, she was the co-recipient of the 2014 Nobel Peace Prize, along with Kailash Satyarthi of India. Aged 17 at the time, this made her the youngest-ever Nobel Prize laureate. In 2015, Yousafzai was a subject of the Oscar-shortlisted documentary He Named Me Malala. The 2013, 2014 and 2015 issues of Time magazine featured her as one of the most influential people globally. In 2017, she was awarded honorary Canadian citizenship and became the youngest person to address the House of Commons of Canada. Yousafzai attended Edgbaston High School from 2013 to 2017, and is currently studying for a bachelor's degree in Philosophy, Politics and Economics at Lady Margaret Hall, Oxford.</p>	
			

