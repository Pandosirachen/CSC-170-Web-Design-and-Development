			<h2>Medical treatment</h2>
				<p>After the shooting, Yousafzai was airlifted to a military hospital in Peshawar, where doctor. were forced to begin operating after swelling developed in the left portion of her brain, which had been damaged by the bullet when it passed through her head. After a five-hour operation, doctors successfully removed the bullet, which had lodged in her shoulder near her spinal cord. The day following the attack, doctors performed a decompressive craniectomy, in which part of her skull was removed to allow room for swelling. On 11 October 2012, a panel of Pakistani and British doctors decided to move Yousafzai to the Armed Forces Institute of Cardiology in Rawalpindi. Mumtaz Khan, a doctor, said that she had a 70% chance of survival. Interior Minister Rehman Malik said that Yousafzai would be moved to Germany, where she could receive the best medical treatment, as soon as she was stable enough to travel. A team of doctors would travel with her, and the government would bear the cost of her treatment. Doctors reduced Yousafzai's sedation on 13 October, and she moved all four limbs.</p>

				<p>Offers to treat Yousafzai came from around the world. On 15 October, Yousafzai travelled to the United Kingdom for further treatment, approved by both her doctors and family. Her plane landed in Birmingham, England where she was treated at the Queen Elizabeth Hospital, one of the specialties of this hospital being the treatment of military personnel injured in conflict.According to media reports at the time, the UK Government stated that "the Pakistani government is paying all transport, migration, medical, accommodation and subsistence costs for Malala and her party."Yousafzai had come out of her coma by 17 October 2012, was responding well to treatment, and was said to have a good chance of fully recovering without any brain damage. Later updates on 20 and 21 October stated that she was stable, but was still battling an infection.[6By 8 November, she was photographed sitting up in bed. On 11 November, Yousafzai underwent surgery for eight and a half hours, in order to repair her facial nerve.</p>

				<p>On 3 January 2013, Yousafzai was discharged from the hospital to continue her rehabilitation at her family's temporary home in the West Midlands,where she had weekly physiotherapy. She underwent a five-hour long operation on 2 February to reconstruct her skull and restore her hearing with a cochlear implant, after which she was reported to be in stable condition.Yousafzai wrote in July 2014 that her facial nerve had recovered up to 96%.</p>

