			<div class="p2">
				<p><strong>Yousafzai </strong> was born on 12 July 1997 in the Swat District of Pakistan's northwestern Khyber Pakhtunkhwa province, into a lower-middle-class family. She is the daughter of Ziauddin Yousafzai and Tor Pekai Yousafzai. Her family is Sunni Muslim of Pashtun ethnicity.The family did not have enough money for a hospital birth and as a result, Yousafzai was born at home with the help of neighbours. She was given her first name Malala (meaning "grief-stricken") after Malalai of Maiwand, a famous Pashtun poet and warrior woman from southern Afghanistan.Her last name, Yousafzai, is that of a large Pashtun tribal confederation that is predominant in Pakistan's Swat Valley, where she grew up. At her house in Mingora, she lived with her two younger brothers, Khushal and Atal, her parents, Ziauddin and Toor Pekai, and two pet chickens.</p>

				<p>Fluent in Pashto, Urdu and English, Yousafzai was educated mostly by her father, Ziauddin Yousafzai, who is a poet, school owner, and an educational activist himself, running a chain of private schools known as the Khushal Public School. In an interview, Yousafzai once stated that she aspired to become a doctor, though later her father encouraged her to become a politician instead. Ziauddin referred to his daughter as something entirely special, allowing her to stay up at night and talk about politics after her two brothers had been sent to bed.</p>

				<p>Inspired by Muhammad Ali Jinnah and Prime Minister Benazir Bhutto, Yousafzai started speaking about education rights as early as September 2008, when her father took her to Peshawar to speak at the local press club."How dare the Taliban take away my basic right to education?", Yousafzai asked her audience in a speech covered by newspapers and television channels throughout the region.In 2009, Yousafzai began as a trainee and then a peer educator in the Institute for War and Peace Reporting's Open Minds Pakistan youth programme, which worked in schools in the region to help young people engage in constructive discussion on social issues through the tools of journalism, public debate and dialogue.</p>
