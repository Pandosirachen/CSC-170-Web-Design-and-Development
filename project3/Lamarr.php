			<h2>Early life</h2>
				<p>Lamarr was born Hedwig Eva Maria Kiesler in 1914 in Vienna, Austria-Hungary, the only child of Gertrud "Trude" Kiesler (née Lichtwitz; 1894–1977) and Emil Kiesler (1880–1935). Her father was born to a Galician Jewish family in Lemberg (now Lviv in Ukraine) and was a successful bank director.</p>
				<p>Trude, her mother, a pianist and Budapest native, had come from an upper-class Hungarian-Jewish family. She had converted to Catholicism and was described as a "practicing Christian" who raised her daughter as a Christian. Lamarr helped get her mother out of Austria after it had been absorbed by the Third Reich and to the United States, where Gertrude later became an American citizen. She put "Hebrew" as her race on her petition for naturalization, which was a term often used in Europe.</p>
				<p>As a child, Lamarr showed an interest in acting and was fascinated by theatre and film. At the age of 12, she won a beauty contest in Vienna.</p>
			</section>
		
