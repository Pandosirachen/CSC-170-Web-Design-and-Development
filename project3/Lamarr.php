				<h2>European Film Career</h2>
				<p>Lamarr was taking acting classes in Vienna when one day, she forged a note from her mother and went to Sascha-Film and was able to get herself hired as a script girl. While there, she was able to get a role as an extra in Money on the Street(1930), and then a small speaking part in Storm in a Water Glass (1931). Producer Max Reinhardt then cast her in a play entitled The Weaker Sex, which was performed at the Theater in der Josefstadt. Reinhardt was so impressed with her that he brought her with him back to Berlin. </p>
				<p>However, she never actually trained with Reinhardt or appeared in any of his Berlin productions. Instead, she met the Russian theatre producer Alexis Granowsky, who cast her in his film directorial debut, The Trunks of Mr. O.F. (1931), starring Walter Abel and Peter Lorre.[16] Granowsky soon moved to Paris, but Lamarr stayed in Berlin and was given the lead role in No Money Needed (1932), a comedy directed by Carl Boese. Lamarr starred in the film which made her internationally famous.</p>
				<p>In early 1933, at age 18, Lamarr was given the lead in Gustav Machatý's film, Ecstasy (Ekstase in German, Extase in Czech). She played the neglected young wife of an indifferent older man.</p>
				<p>The film became both celebrated and notorious for showing Lamarr's face in the throes of orgasm as well as close-up and brief nude scenes, a result of her being "duped" by the director and producer, who used high-power telephoto lenses.</p>
				<p>Although she was dismayed and now disillusioned about taking other roles, the film gained world recognition after winning an award in Rome. Throughout Europe, it was regarded an artistic work. In America it was considered overly sexual and received negative publicity, especially among women's groups.[18] It was banned there and in Germany.</p>
				<p>Lamarr played a number of stage roles, including a starring one in Sissy, a play about Empress Elisabeth of Austriaproduced in Vienna. It won accolades from critics. Admirers sent roses to her dressing room and tried to get backstage to meet her. She sent most of them away, including a man who was more insistent, Friedrich Mandl. He became obsessed with getting to know her. </p>
				<p>Mandl was an Austrian military arms merchant and munitions manufacturer who was reputedly the third-richest man in Austria. She fell for his charming and fascinating personality, partly due to his immense financial wealth. Her parents, both of Jewish descent, did not approve, due to Mandl's ties to Italian fascist leader Benito Mussolini, and later, German Führer Adolf Hitler, but they could not stop the headstrong Lamarr. </p>
				<p>On August 10, 1933, Lamarr married Mandl. She was 18 years old and he was 33. In her autobiography Ecstasy and Me, she described Mandl as an extremely controlling husband who strongly objected to her simulated orgasm scene in Ecstasyand prevented her from pursuing her acting career. She claimed she was kept a virtual prisoner in their castle home, Schloss Schwarzenau.</p>

