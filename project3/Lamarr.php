			</figure>
			<div class="p1">
				<p>Although Lamarr had no formal training and was primarily self-taught, she worked in her spare time on various hobbies and inventions, which included an improved traffic stoplight and a tablet that would dissolve in water to create a carbonated drink. The beverage was unsuccessful; Lamarr herself said it tasted like Alka-Seltzer.</p>
				<p>Among the few who knew of Lamarr's inventiveness was aviation tycoon Howard Hughes. She suggested he change the rather square design of his aeroplanes (which she thought looked too slow) to a more streamlined shape, based on pictures of the fastest birds and fish she could find. Lamarr discussed her relationship with Hughes during an interview, saying that while they dated, he actively supported her "tinkering" hobbies. He put his team of scientists and engineers at her disposal, saying they would do or make anything she asked for.</p>
			</div>
				<p class="subp1">During World War II, Lamarr learned that radio-controlled torpedoes, an emerging technology in naval war, could easily be jammed and set off course. She thought of creating a frequency-hopping signal that could not be tracked or jammed. She contacted her friend, composer and pianist George Antheil, to help her develop a device for doing that, and he succeeded by synchronizing a miniaturized player-piano mechanism with radio signals. They drafted designs for the frequency-hopping system, which they patented. Antheil recalled: We began talking about the war, which, in the late summer of 1940, was looking most extremely black. Hedy said that she did not feel very comfortable, sitting there in Hollywood and making lots of money when things were in such a state. She said that she knew a good deal about munitions and various secret weapons ... and that she was thinking seriously of quitting MGM and going to Washington, DC, to offer her services to the newly established Inventors' Council.</p>

				<p class="subp1">Their invention was granted a patent under US Patent 2,292,387 on August 11, 1942 (filed using her married name Hedy Kiesler Markey). However, it was technologically difficult to implement, and at that time the U.S. Navy was not receptive to considering inventions coming from outside the military. In 1962, (at the time of the Cuban missile crisis), an updated version of their design at last appeared on Navy ships.</p>
				<p class="subp1">In 1997, Lamarr and Antheil received the Electronic Frontier Foundation Pioneer Award and the Bulbie Gnass Spirit of Achievement Bronze Award, given to individuals whose creative lifetime achievements in the arts, sciences, business, or invention fields have significantly contributed to society. Lamarr was featured on the Science Channel and the Discovery Channel. In 2014, Lamarr and Antheil were posthumously inducted into the National Inventors Hall of Fame.</p>
				<p class="subp1">Mandl had close social and business ties to the Italian government, selling munitions to the country, and although like Hedy, his own father was Jewish, had ties to the Nazi regime of Germany, as well. Lamarr wrote that the dictators of both countries attended lavish parties at the Mandl home. Lamarr accompanied Mandl to business meetings, where he conferred with scientists and other professionals involved in military technology. These conferences were her introduction to the field of ,applied science and nurtured her latent talent in science. </p>
				<p class="subp1">Lamarr's marriage to Mandl eventually became unbearable, and she decided to separate herself from both her husband and country. In her autobiography, she wrote that she disguised herself as her maid and fled to Paris, but by other accounts, she persuaded Mandl to let her wear all of her jewelry for a dinner party, then disappeared afterward. She writes about her marriage:I knew very soon that I could never be an actress while I was his wife. ... He was the absolute monarch in his marriage. ... I was like a doll. I was like a thing, some object of art which had to be guarded—and imprisoned—having no mind, no life of its own.</p>
			
